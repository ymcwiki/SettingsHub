local cache = {}
-- 返回该 CVar 的主题 key(带缓存);未命中返回 "other"
function ns.Data.ClassifyTopic(name)
	local hit = cache[name]
	if hit ~= nil then return hit end
	local n = name:lower()
	for _, rule in ipairs(ns.Data.topicRules) do
		for _, pat in ipairs(rule.patterns) do
			if pat.prefix then
				if n:sub(1, #pat.s) == pat.s then cache[name] = rule.key return rule.key end
			else
				if n:find(pat.s, 1, true) then cache[name] = rule.key return rule.key end
			end
		end
	end
	cache[name] = "other"
	return "other"
end

-- 主题 key -> 中文名(含其他)
local names = { other = ns.Data.TOPIC_OTHER.zh }
for _, t in ipairs(ns.Data.topicOrder) do names[t.key] = t.zh end
function ns.Data.TopicName(key) return names[key] or key end
